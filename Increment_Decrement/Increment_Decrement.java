import java.util.*;
class Increment_Decrement
{
	public static void main(String args[])
	{
		int a=3;
		System.out.println("The value of a1= "+ a);
		System.out.println("The value of a1= "+ a--); // Post_Increment
		System.out.println("The value of a1= "+ a);
		System.out.println("The value of a1= "+ --a); //Pre_Increment
		System.out.println("The value of a1= "+ a);
		System.out.println("The value of a1= "+ a--);			
		System.out.println("The value of a1= "+ a);
	}
}