public class Local_variable_2 
{ 
	public void StudentAge() 
	{ 
		// local variable age 
		int age = 0; 
		age = age + 5; 
		System.out.println("Student age is : " + age); 
	} 

	public static void main(String args[]) 
	{ 
		Local_variable_2  obj = new Local_variable_2 (); 
		obj.StudentAge(); 
	} 
} 
